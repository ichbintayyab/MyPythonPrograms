# Assignment Operators
x = 5
print("Initial x:", x)

x += 2      # x = x + 2
print("After x += 2;", x)

x -= 1      # x = x - 1
print("After x -= 1;", x)

x *= 3      # x = x * 3
print("After x *= 3", x)

x /= 2      # x = x / 2
print("After x /= 2:", x)

x %= 4      # x = x % 4
print("After x /= 4:", x)