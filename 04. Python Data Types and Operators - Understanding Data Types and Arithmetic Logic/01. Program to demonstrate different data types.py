# Different data types in Python

# Integer
num = 10
print("Integer:", num)

# Float
pi = 3.14
print("Float:", pi)

# String
name = "Sandman"
print("String:", name)

# Boolean
is_student = True
print("Boolean:", is_student)

# List 
fruits = ["apple","banana","cherry"]
print("Lists:", fruits)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Dictionary
person = {"Name":"Bob", "age":25}
print("Dictionary:", person)

# Set
unique_numbers = {1, 2, 3}
print("Set:", unique_numbers)