# Creating two sets
set1 = {1, 3, 5}
set2 = {2, 4, 6}

# Performing union set using | - Method 1
union_set = set1 | set2 


# Displaying the result
print("Union of set1 and set2:", union_set)

# Another method to performing union set using .union() - Method 1
print("Union of set 1 and 2 using .union():", set1.union(set2))