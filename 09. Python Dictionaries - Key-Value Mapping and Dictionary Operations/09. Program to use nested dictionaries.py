# Nested dictionary of students
students = {
    "student1": {
        "name": "Sandman",
        "age": 20
    },
    "student2": {
        "name": "The Dreamer",
        "age": 100
    }
}

# Accessing nested values
print("Student1 Name:", students["student1"]["name"])
print("Student2 Age:", students["student2"]["age"])